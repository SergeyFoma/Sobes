'''Словари (dict) в Python и формат JSON (JavaScript Object Notation) 
различаются по назначению и синтаксису. 
Хотя оба формата представляют данные в виде пар «ключ-значение», 
они имеют разные цели.

Словари (dict)
Предназначены для хранения и обработки данных внутри Python. Некоторые особенности словарей: 
blog.mikihands.com
Изменяемость — словари можно модифицировать после создания.
Уникальность ключей — каждый ключ в словаре должен быть уникальным.
Хешируемость ключей — ключи должны быть неизменяемыми объектами (строки, числа, кортежи с неизменяемыми элементами).
Неупорядоченность — до Python 3.7 порядок элементов не гарантировался (с 3.7 словари сохраняют порядок вставки).
Произвольные значения — значения могут быть любого типа, включая другие словари, списки или пользовательские объекты.

JSON
Предназначен для обмена данными между системами, языками или приложениями. Некоторые особенности JSON: 
pythontutorials.net
Синтаксис ключей — только строки (в двойных кавычках).
Поддерживаемые типы значений — строки, числа, массивы, объекты, true, false, null.
Комментарии — не поддерживаются.
Вложенность — неограниченная (в пределах разумного).
 
Важно: не все данные Python напрямую сериализуются в JSON. Например: 
datetime-объекты — нужно предварительно преобразовать в строки;
комплексные числа — не имеют прямого аналога в JSON;
объекты с методами dict — требуют особого подхода;
байтовые строки — должны быть декодированы в обычные строки.
 
В Python для работы с JSON используется встроенная библиотека json, которая позволяет сериализовать (преобразовать в JSON) 
объекты Python и десериализовать (преобразовать из JSON) в объекты Python. 
Таким образом: словари подходят для манипуляций с данными внутри программы, а JSON — для обмена данными между системами. 
'''
# https://blog.mikihands.com/ru/whitedec/2024/12/15/json-vs-python-dictionary/
import json

person = {
    'name':'Durio',
    'age':25,
    'job':None,
    'hobby':True,
    'list':[1,2,3]
}
print(person)
person_json = json.dumps(person)
print(person_json)


connect_db={
    'database':{
        'db_name':'asdf',
        'port':5432,
        'host':'127.0.0.1'
    },
    'connect':150,
    'debug':True,
    'zxc':(2,3,4)
}
connect_db_json=json.dumps(connect_db)
print(type(connect_db_json))

## Записываем JSON в файл
with open("connect_db_json.json", 'w')as f:
    json.dump(connect_db, f)

with open("connect_db_json.json")as f:
    print("READ: ", json.loads(f.read()))

with open("connect_db_json.json", "r")as f:
    fread=json.load(f)
    print('FREAD: ',type(fread))

